    public LineSorter getSorter() {
        int sel = orderBox.getSelectedIndex();

        if (sel < 0) {
            return null;
        }

        boolean descending = descOrderBtn.isSelected();
        if (sel == 0) {
            return new LineSorter(descending);
        }

        Locale locale;
        if (useUIBtn.isSelected()) {
            locale = Language.getInterfaceLocale();
        } else if (useGameBtn.isSelected()) {
            locale = Language.getGameLocale();
        } else {
            locale = ((LanguageCodeDescriptor) customLocaleCombo.getSelectedItem()).getLocale();
        }
        if (locale == null) {
            StrangeEons.log.severe("null locale");
            locale = Locale.getDefault();
        }

        final int[] strengths = new int[]{
            Collator.PRIMARY,
            Collator.SECONDARY,
            Collator.TERTIARY,
            Collator.IDENTICAL
        };

        --sel;
        if (sel < 4) {
            return new LineSorter.LocalizedSorter(locale, strengths[sel], descending);
        } else {
            return new LineSorter.SemanticSorter(locale, strengths[sel - 4], descending);
        }
    }

