    private void initLocaleCombo() {
        // check if the locale combo was already loaded
        if (!(customLocaleCombo.getModel().getElementAt(0) instanceof String)) {
            return;
        }

        JUtilities.showWaitCursor(this);
        try {
            DefaultComboBoxModel m = new DefaultComboBoxModel();
            for (Locale loc : Language.getLocales()) {
                m.addElement(new LanguageCodeDescriptor(loc, false, true));
            }
            customLocaleCombo.setModel(m);
            customLocaleCombo.setSelectedIndex(0);
            pack();
        } finally {
            JUtilities.hideWaitCursor(this);
        }
    }

